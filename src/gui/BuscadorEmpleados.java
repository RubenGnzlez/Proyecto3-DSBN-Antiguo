    private void querySearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_querySearchActionPerformed
        	try {
                        String lastName = lastNameTXT.getText();

                        List<Empleado> employees = null;

                        if (lastName != null && lastName.trim().length() > 0) {
                                employees = empleado.searchEmployees(lastName);
                        } else {
                                employees = empleado.getAllEmployees();
                        }

                        // create the model and update the "table"
                        ModeloTablaEmpleado model = new ModeloTablaEmpleado(employees);

                        jTable1.setModel(model);

                        /*
                        for (Employee temp : employees) {
                                System.out.println(temp);
                        }
                        */
                } catch (Exception exc) {
                        JOptionPane.showMessageDialog(BuscadorEmpleados.this, "Error: " + exc, "Error", JOptionPane.ERROR_MESSAGE); 
                }
    }//GEN-LAST:event_querySearchActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// get the selected item
                int row = jTable1.getSelectedRow();

                // make sure a row is selected
                if (row < 0) {
                        JOptionPane.showMessageDialog(BuscadorEmpleados.this, "You must select an employee", "Error",
                                        JOptionPane.ERROR_MESSAGE);				
                        return;
                }

                // get the current employee
                Empleado tempEmployee = (Empleado) jTable1.getValueAt(row, ModeloTablaEmpleado.OBJECT_COL);

                // create dialog
                AñadirEmpleado dialog = new AñadirEmpleado(BuscadorEmpleados.this, empleado,tempEmployee, true);

                // show dialog
                dialog.setVisible(true);
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();

        // make sure a row is selected
        if (row < 0) {
                JOptionPane.showMessageDialog(BuscadorEmpleados.this, "You must select an employee", "Error",
                                JOptionPane.ERROR_MESSAGE);				
                return;
        }
         Empleado tempEmployee = (Empleado) jTable1.getValueAt(row, ModeloTablaEmpleado.OBJECT_COL);
         
        try {
            empleado.deleteEmployee(tempEmployee.getEmail());
            refreshEmployeesView();
        } catch (SQLException ex) {
            Logger.getLogger(BuscadorEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
         
         
         
       
    }//GEN-LAST:event_jButton3ActionPerformed

